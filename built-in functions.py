# append function
list1.append()  # <variable>.<function>, adds at the end
print(list1)

# count function
print(list1.count(1))

# remove function
list1.remove('hello')  # accepts direct value

# index function
y = list1.index('hello')
print(y)

# pop function
list1.pop(2)  # accepts index value
print(list1)

# extend function
list2 = [6, 8, 9, 7]
list1.extend(list2)  # add another list in list
print(list1)

# sort function
list1.sort()  # True - yes and false - no
print(list2)
